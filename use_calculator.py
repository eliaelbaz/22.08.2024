import calculator

result_add = calculator.add(5, 3)
result_subtract = calculator.subtract(10, 2)
result_multiply = calculator.multiply(4, 6)
result_divide = calculator.divide(20, 4)
result_power = calculator.power(2, 3)
result_sqrt = calculator.sqrt(16)
result_is_prime = calculator.is_prime(13)
result_factorial = calculator.factorial(5)
print(f"5 + 3 = {result_add}")
print(f"10 - 2 = {result_subtract}")
print(f"4 * 6 = {result_multiply}")
print(f"20 / 4 = {result_divide}")
print(f"2 ** 3 = {result_power}")
print(f"sqrt(16) = {result_sqrt}")
print(f"13 is prime: {result_is_prime}")
print(f"Factorial of 5 is {result_factorial}")


